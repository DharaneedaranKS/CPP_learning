#include <iostream>
#include <math.h>

using namespace std;

void sizevariable()
{
    cout<<"The size of integer is: "<<sizeof(int)<<"\n";
    cout<<"The size of char is: "<<sizeof(char)<<"\n";
    cout<<"The size of float is: "<<sizeof(float)<<"\n";
    cout<<"The size of double is: "<<sizeof(double)<<"\n";
}

void main()
{
    sizevariable();

}

